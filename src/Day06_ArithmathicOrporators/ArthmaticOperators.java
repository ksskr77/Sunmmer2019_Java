package Day06_ArithmathicOrporators;

public class ArthmaticOperators {

	public static void main(String[] args) {
		 
		/*
		 + : addition, adds two numbers together
		 - : subtraction, subtracts a number from another number
		 * : multiplication
		 / : division
		 
		 % : remainder returns the remainder from the division
		 
		 */
		
		int a = 100;
		int b = a - 400; // -300;
		System.out.println(b);
		
		int c = a + b;
		System.out.println(c);
		
		int d = a*b;
		System.out.println(d);
		
		double e = a/b;
		System.out.println(e);
		
		
		int x = 3, y = 5, z= x*y;
		
		System.out.println(z);
		
		int length = 100, width = 10, area = length/width;
		System.out.println(area);
		
		System.out.println(3+4+5+6);
		
		double resultNum = 9 / 3; // 3.0
		System.out.println(resultNum);
	
		double Number1 = 10.0/3; // 3.33333
		System.out.println(Number1);
		
		int output = 12+6/3;
		System.out.println(output);
		
		int result1 = 10%3; //1
		System.out.println(result1);
		System.out.println(12%5);
		
		/*
		  10/3 = 3 with remainder of 1
		  12/5 = 2 with remainder of 2
		  20/3 = 6 with remainder of 2
		 */
		
		System.out.println(20%3);
		
		//20, 45, 65, 67, 68
		
		
		int Number4 = 20%2;
		int Number5 = 29%2;
		
		System.out.println(20%2);
		
		
		
	}
	
	
}
